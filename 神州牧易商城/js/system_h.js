// JavaScript Document
$(document).ready(function(){
    var $x=$("#sy01");
    var $y=$("#so01");          //获取 显示 按钮
    $y.click(function(){
        if($x.is(":visible")){           //如果元素显示
            $x.hide();
            $("#so01 span").text("更多");            
        }else{
            $x.show();
            $("#so01 span").text("收起");
        }
    });

});

